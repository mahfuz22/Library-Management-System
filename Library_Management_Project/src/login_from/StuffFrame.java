    int getBookID(String bName) {
        int bookId = 0;
        stmt = null;
        con = ConnectionDb.getConnection();
        rs = null;
        try {
            stmt = con.createStatement();
            rs = stmt.executeQuery(" select b_id from books where book_name='" + bName + "' ");
            while (rs.next()) {
                bookId = bookId + rs.getInt(1);

            }

        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                rs.close();
                stmt.close();
            } catch (SQLException ex) {
                System.out.println(ex);
            }
        }
        return bookId;
    }

