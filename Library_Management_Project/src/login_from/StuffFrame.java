    private void btnSaleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaleActionPerformed
        //sale product and save info into sale table...

        String getBookName = txtBookNameSale.getText();
        String getqty = txtqtysale.getText();
        String getDate = txtDate.getText();
        String getTotalPrice = txtTotalPrice.getText();
        int bookId = getBookID(getBookName);
        // System.out.println("book id ="+bookId);
        stmt = null;
        con = ConnectionDb.getConnection();
        try {
            int count = 0;
            stmt = con.createStatement();
            count = stmt.executeUpdate("insert into sale (book_name, qty, sale_date, total_price, b_id) "
                    + "values('" + getBookName + "','" + getqty + "','" + getDate + "','" + getTotalPrice + "','" + bookId + "')");
            if (count > 0) {
                JOptionPane.showMessageDialog(rootPane, "Sale info inserted into sale table !!!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Please fillup all field above");
        } finally {
            try {
                stmt.close();
                con.close();
            } catch (SQLException ex) {
                System.out.println(ex);
            }
        }


    }//GEN-LAST:event_btnSaleActionPerformed

